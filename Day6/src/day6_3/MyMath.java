package day6_3;

public class MyMath {
	long add(long a, long b) {
		long result = a+b;
		return result;
		// return a+b' 위의 두 줄을 이렇게 한줄로 정리할수 있다.
	}
	
	long subtract(long a, long b) {return a - b;}
	long multiply(long a, long b) {return a - b;}
	double divide(double a, double b) {
		return a / b;
	}
	
}
